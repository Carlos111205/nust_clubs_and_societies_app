class ClubEvent {
  final String id;
  final String clubId;
  final String title;
  final String date;
  final String time;
  final String location;
  final String description;

  ClubEvent({
    required this.id,
    required this.clubId,
    required this.title,
    required this.date,
    required this.time,
    required this.location,
    required this.description,
  });
}
